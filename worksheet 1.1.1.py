#1.	Write a  program  to enter two numbers and perform all arithmetic operations.

a = eval(input("Enter a number : "))
b = eval(input("Enter a number : "))

print("\n")
print(a, " + ", b, " = ", a + b)
print(a, " - ", b, " = ", a - b)
print(a, " * ", b, " = ", a * b)
print(a, " / ", b, " = ", a / b)
print(a, " ^ ", b, " = ", a ** b)
print(a, " // ", b, " = ", a // b)
print(a, " % ", b, " = ", a % b)
